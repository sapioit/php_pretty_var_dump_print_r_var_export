function pr($data)
{
    echo "<pre>";
    print_r($data);
    echo "</pre>";
}

function vd($data)
{
    echo "<pre>";
    var_dump($data);
    echo "</pre>";
}

function ve($data)
{
    echo "<pre>";
    var_export($data);
    echo "</pre>";
}

function pr2($data)
{
    echo "<pre><code>";
    print_r($data);
    echo "</code></pre>";
}

